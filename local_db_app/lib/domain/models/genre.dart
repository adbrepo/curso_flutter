import 'package:floor/floor.dart';

@entity
class Genre {
  @PrimaryKey(autoGenerate: true)
  final int id;
  final String name;

  Genre({
    required this.id,
    required this.name,
  });

  factory Genre.fromJson(Map<String, dynamic> json) {
    return Genre(
      id: json['id'],
      name: json['name'],
    );
  }
}
